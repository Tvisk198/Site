const CATALOG_PAGE_SIZE = 20;
let catalogCurrentPage = 1;
let filteredData = [...gameData];

function applySidebarFilters() {
  const checkedPlatforms = [];
  if(document.getElementById('f-pc')?.checked) checkedPlatforms.push('PC');
  if(document.getElementById('f-ps5')?.checked) checkedPlatforms.push('PS5');
  if(document.getElementById('f-ps4')?.checked) checkedPlatforms.push('PS4');
  if(document.getElementById('f-xbox')?.checked) checkedPlatforms.push('Xbox');
  if(document.getElementById('f-mobile')?.checked) checkedPlatforms.push('Mobile');
  if(document.getElementById('f-switch')?.checked) checkedPlatforms.push('Switch');

  const checkedPrices = [];
  if(document.getElementById('p1')?.checked) checkedPrices.push([0,10]);
  if(document.getElementById('p2')?.checked) checkedPrices.push([10,25]);
  if(document.getElementById('p3')?.checked) checkedPrices.push([25,50]);
  if(document.getElementById('p4')?.checked) checkedPrices.push([50,9999]);

  const checkedGenres = [];
  const genreMap = {g1:'Action',g2:'Adventure',g3:'Arcade',g4:'Fighting',g5:'Racing',g6:'RPG',g7:'Strategy',g8:'Sports'};
  Object.entries(genreMap).forEach(([id,name])=>{if(document.getElementById(id)?.checked) checkedGenres.push(name);});

  let data = [...gameData];
  const allPlats = activePlatformFilter ? [activePlatformFilter] : checkedPlatforms;
  if(allPlats.length) data = data.filter(g=>allPlats.includes(g.platform));
  if(checkedPrices.length) data = data.filter(g=>checkedPrices.some(([mn,mx])=>g.price>=mn&&g.price<mx));
  if(checkedGenres.length) data = data.filter(g=>checkedGenres.some(genre=>g.genre.includes(genre)));

  const sortVal = document.getElementById('sort-select')?.value||'popular';
  if(sortVal==='priceAsc') data.sort((a,b)=>a.price-b.price);
  else if(sortVal==='priceDesc') data.sort((a,b)=>b.price-a.price);
  else if(sortVal==='newest') data.sort((a,b)=>b.id-a.id);

  filteredData = data;
  catalogCurrentPage = 1;
  renderCatalogPage();
}

function renderCatalogPage() {
  const grid = document.getElementById('catalog-grid');
  if(!grid) return;
  const total = filteredData.length;
  const totalPages = Math.max(1,Math.ceil(total/CATALOG_PAGE_SIZE));
  if(catalogCurrentPage>totalPages) catalogCurrentPage=totalPages;
  const start = (catalogCurrentPage-1)*CATALOG_PAGE_SIZE;
  const slice = filteredData.slice(start, start+CATALOG_PAGE_SIZE);
  grid.innerHTML = slice.map(g=>buildGameCard(g)).join('');
  updateCatalogCount();
  renderPagination(totalPages);
  window.scrollTo({top:0,behavior:'smooth'});
}

function renderPagination(totalPages) {
  const el = document.getElementById('pagination');
  if(!el) return;
  if(totalPages<=1){el.innerHTML='';return;}
  const cur = catalogCurrentPage;
  let html = '';
  const pages = [];
  pages.push(1);
  if(cur-2>2) pages.push('...');
  for(let i=Math.max(2,cur-1);i<=Math.min(totalPages-1,cur+1);i++) pages.push(i);
  if(cur+2<totalPages-1) pages.push('...');
  if(totalPages>1) pages.push(totalPages);
  pages.forEach(p=>{
    if(p==='...') html+=`<span class="page-btn dots">…</span>`;
    else html+=`<button class="page-btn${p===cur?' active':''}" onclick="goPage(${p})">${p}</button>`;
  });
  el.innerHTML=`<button class="page-btn" onclick="goPage(${cur-1})" ${cur===1?'disabled':''}>‹</button>${html}<button class="page-btn" onclick="goPage(${cur+1})" ${cur===totalPages?'disabled':''}>›</button>`;
}

function goPage(n) {
  const total = Math.ceil(filteredData.length/CATALOG_PAGE_SIZE);
  if(n<1||n>total) return;
  catalogCurrentPage=n;
  renderCatalogPage();
}

