function doSpin() {
  if(isSpinning) return;
  isSpinning = true;
  const btn = document.getElementById('spin-go-btn');
  btn.disabled = true;
  btn.textContent = t('spinSpinning');
  document.getElementById('spin-result').textContent = '';
  const extraSpins = 5 + Math.floor(Math.random()*4);
  const targetIdx = Math.floor(Math.random()*spinPrizes.length);
  const arc = (2*Math.PI)/spinPrizes.length;
  const targetAngle = -targetIdx*arc - arc/2 + Math.PI/2;
  const finalAngle = extraSpins*2*Math.PI + targetAngle;
  const duration = 4000;
  const startAngle = spinAngle;
  const startTime = performance.now();
  function animate(now) {
    const elapsed = now - startTime;
    const progress = Math.min(elapsed/duration,1);
    const ease = 1-Math.pow(1-progress,4);
    spinAngle = startAngle + (finalAngle-startAngle)*ease;
    drawWheel();
    if(progress<1) { requestAnimationFrame(animate); return; }
    spinAngle = finalAngle;
    isSpinning = false;
    btn.disabled = false;
    btn.textContent = t('spinBtn');
    document.getElementById('spin-result').textContent = t(spinPrizes[targetIdx].label);
  }
  requestAnimationFrame(animate);
}

